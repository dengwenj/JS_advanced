/**
 * 确定的输入，一定会产生确定的输出
 * 函数在执行过程中，不能产生副作用
 * 
 * 在计算机科学中，也引用了副作用的概念，表示在执行一个函数时，除了返回函数值之外，还对调用函数产生了附加的影响，
 * 比如修改了全局变量，修改参数或者改变外部的存储
 */

/**
 * 数组 slice 和 splice 就是一个对比
 * slice 是一个纯函数，因为不会修改原数组
 * splice 不是一个纯函数，因为会修改原数组
 */

// 这就不是一个纯函数
function HelloWorld(props) {
  props.name = "dwj"
}
const obj = {
  name: "朴睦",
  age: 23
}
HelloWorld(obj)
